public class Vehicle
{

    private Double lng;
    private Double ltd;


    public Vehicle()
    {
    }


    public Double getLng()
    {
        return lng;
    }

    public void setLng(Double lng)
    {
        this.lng = lng;
    }

    public Double getLtd()
    {
        return ltd;
    }

    public void setLtd(Double ltd)
    {
        this.ltd = ltd;
    }


}
